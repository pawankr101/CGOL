/**
 * @class `GenerationData`
 * @typedef { { rows: number, cols: number, buffer: ArrayBuffer, data: Uint8Array, setRow: (rowIndex: number, values: number[]) => void } } GenerationData
 * @typedef { { new (rows: number, cols: number) => GenerationData } } GenerationDataConstructor
 * @type { GenerationDataConstructor }
 */
const GenerationData = (function(){
    /**
     * @constructor
     * @param {number} rows 
     * @param {number} cols 
     * @param {ArrayBuffer} [buffer]
     * @returns {GenerationData}
     */
    function GenerationData(rows, cols) {
        if(!new.target) throw new Error('Use `new` keyword to create Object of this class.');

        /** @type {number} */
        this.rows = rows;

        /** @type {number} */
        this.cols = cols;

        /** @type {ArrayBuffer} */
        this.buffer =  new ArrayBuffer(rows * cols);

        /** @type {Uint8Array} */
        this.data = new Uint8Array(this.buffer);
    }

    /** 
     * @param {number} rowIndex
     * @param {number[]} values
     */
    GenerationData.prototype.setRow = function(rowIndex, values) {
        this.data.set(values, (rowIndex * this.cols));
    }
    return GenerationData;
})();

/**
 * @class `Generation`
 * @typedef { { data: GenerationData, snapshot: (0|1)[][], initialize(rows: number, cols: number, initialState?: (0 | 1)[][])=>void, sendSnapshotData()=>void, reset()=>void } } Generation
 * @typedef { { new () => Generation, init: (rows: number, cols: number, initialState?: (0 | 1)[][]) => Generation } } GenerationConstructor
 * @type { GenerationConstructor }
 */
const Generation = (function() {

    /**
     * @param {number} row 
     * @param {number} col 
     * @param {number} totalRows 
     * @param {number} totalCols 
     * @param {(0|1)[][]} data 
     * @returns {0|1}
     */
    const nextGenValue = function(row, col, totalRows, totalCols, data) {
        const iMin = (row>0) ? (row-1) : 0, iMax = (row < totalRows-1) ? (row+1) : row;
        const jMin = (col>0) ? (col-1) : 0, jMax = (col < totalCols-1) ? (col+1) : col;
        const value = data[row][col];
        let neighbors = 0;
        for(let i=iMin, j; i<=iMax; i++) {
            for(j=jMin; j<=jMax; j++){
                if(i!=row || j!=col) {
                    neighbors += data[i][j];
                    if(neighbors>3) return 0;
                }
            }
        }
        return (neighbors===3) ? 1 : ((neighbors===2) ? value : 0);
    }

    /**
     * @param {number} rows 
     * @param {number} cols 
     * @param {(0|1)[][]} snapshot 
     * @returns {(0|1)[][]}
     */
    const getNextGenSnapshot = function(rows, cols, snapshot) { // have to solve it recursively
        /** @type {(0|1)[][]} */
        const ns = new Array(rows);
        for (let r=0, c, row; r<rows; r++) {
            for(c=0, row=new Array(cols); c<cols; c++) {
                row[c] = nextGenValue(r, c, rows, cols, snapshot);
            }
            ns[r]=row;
        }
        return ns;
    }

    /** @constructor @returns {Generation} */
    function Generation() {}

    /**
     * @param {number} rows 
     * @param {number} cols
     * @param {(0|1)[][]} [initialState]
     * @returns {Generation}
     */
    Generation.init = function(rows, cols, initialState) {
        const generation=new Generation();
        generation.initialize(rows, cols, initialState);
        return generation;
    }
    
    /**
     * @param {number} rows 
     * @param {number} cols
     * @param {(0|1)[][]} [initialState] 
     */
    Generation.prototype.initialize = function(rows, cols, initialState) {

        /** @type {GenerationData} */
        this.data = new GenerationData(rows, cols);

        /** @type {(0|1)[][]} */
        this.snapshot = new Array(rows);

        for(let r=0,c,row; r<rows; r++) {
            for(row=[],c=0; c<cols; c++) {
                row[c] = initialState ? ((initialState[r] && initialState[r][c]) ? initialState[r][c] : 0) : Math.round(Math.random());
            }
            this.snapshot[r] = row;
        }
        this.sendSnapshotData();
    }

    Generation.prototype.sendSnapshotData = function() {
        if(this.data && this.snapshot) {
            let rows = this.data.rows, cols = this.data.cols;
            for(let r = 0; r<rows; r++) {
                this.data.setRow(r, this.snapshot[r]);
            }
            worker.postMessage({buffer: this.data.buffer});
            setTimeout(() => { this.snapshot = getNextGenSnapshot(rows, cols, this.snapshot); }, 0);
        }
    }

    Generation.prototype.reset = function() {
        setTimeout(() => {
            this.data=null;
            this.snapshot=null;
        }, 0);
    }
    return Generation;
})();

